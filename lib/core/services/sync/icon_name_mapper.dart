  /// 根据Material Icon的codePoint获取图标名称
  ///
  /// [codePoint] Material Icon的codePoint值
  /// 返回图标名称（如 'star', 'home'），
  /// 如果未找到映射返回默认图标 'star'
  static String getIconNameFromCodePoint(int codePoint) {
    return _codePointToName[codePoint] ?? 'star';
  }

  /// 根据图标名称获取codePoint
  ///
  /// [iconName] 图标名称（如 'star', 'home'）
  /// 返回codePoint值，
  /// 如果未找到返回 Icons.star 的 codePoint
  static int getCodePointFromIconName(String iconName) {
    for (final entry in _codePointToName.entries) {
      if (entry.value == iconName) {
        return entry.key;
      }
    }
    // 默认返回 Icons.star 的 codePoint
    return 0xE3C3;
  }

  /// 根据存储的图标值（可能是codePoint字符串或图标名称）获取图标名称
  ///
  /// [iconValue] 从数据库或SharedPreferences读取的图标值
  /// 返回Android小组件期望的图标名称
  static String getIconName(dynamic iconValue) {
    if (iconValue == null) return 'star';

    // 如果是字符串且是纯数字，说明是codePoint字符串
    if (iconValue is String) {
      final number = int.tryParse(iconValue);
      if (number != null) {
        return getIconNameFromCodePoint(number);
      }
      // 否则直接作为图标名称返回
      return iconValue;
    }

    // 如果是int，说明是codePoint
    if (iconValue is int) {
      return getIconNameFromCodePoint(iconValue);
    }

    return 'star';
  }
